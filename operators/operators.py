# Arithmetic Operators

a = 10
b = 5

print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Floor Division:", a // b)
print("Modulus:", a % b)
print("Exponent:", a ** b)

# Comparison Operators

print("Equal:", a == b)
print("Not Equal:", a != b)
print("Greater Than:", a > b)
print("Less Than:", a < b)

# Logical Operators

x = True
y = False

print("AND:", x and y)
print("OR:", x or y)
print("NOT:", not x)

# Assignment Operators

num = 10
num += 5
print("After += :", num)

num -= 3
print("After -= :", num)
